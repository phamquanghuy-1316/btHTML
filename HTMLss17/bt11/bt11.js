// Sử dụng hàm Math.abs()
document.writeln(Math.abs(-5) + "<br>"); // Kết quả: 5, vì Math.abs() trả về giá trị tuyệt đối của số -5
document.writeln(Math.abs(5) + "<br>"); // Kết quả: 5, vì giá trị tuyệt đối của 5 là chính nó
document.writeln(Math.abs(10) + "<br>"); // Kết quả: 10, vì giá trị tuyệt đối của 10 là chính nó

// Sử dụng hàm Math.ceil()
document.writeln(Math.ceil(4.4) + "<br>"); // Kết quả: 5, vì Math.ceil() làm tròn lên số nguyên gần nhất
document.writeln(Math.ceil(5.8) + "<br>"); // Kết quả: 6, vì Math.ceil() làm tròn lên số nguyên gần nhất

// Sử dụng hàm Math.floor()
document.writeln(Math.floor(5.7) + "<br>"); // Kết quả: 5, vì Math.floor() làm tròn xuống số nguyên gần nhất
document.writeln(Math.floor(6.1) + "<br>"); // Kết quả: 6, vì Math.floor() làm tròn xuống số nguyên gần nhất

// Sử dụng hàm Math.round()
document.writeln(Math.round(10.1) + "<br>"); // Kết quả: 10, vì Math.round() làm tròn đến số nguyên gần nhất (10.1 gần 10 hơn 11)
document.writeln(Math.round(20.6) + "<br>"); // Kết quả: 21, vì Math.round() làm tròn đến số nguyên gần nhất (20.6 gần 21 hơn 20)